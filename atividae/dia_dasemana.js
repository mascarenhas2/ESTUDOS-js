let dia = 3

switch (dia) {
    case 1:
        console.log("Domingo: Final de semana")
        break;
    case 2:
        console.log("Segunda Dia útil")
        break;
    case 3:
        console.log("Terça Feira")
        break;
    case 4:
        console.log("Quarta feira")
        break;
    case 5:
        console.log("Quinta feira")
        break;
    case 6:
        console.log("Sexta-Feira")
        break;

    case 7: 
        console.log("Sabado")
        break;
    default:
        console.log("opção invalida.")
        break;
}